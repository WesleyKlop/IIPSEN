package game.cards;


/**
 * Enum containing types of game.cards
 *
 * @author Wesley Klop
 */
public enum CardType {
    /**
     * LOCOMOTIVE is a special type of card
     */
    LOCOMOTIVE,
    /**
     * CART_ANY is valid for any type of CART_
     */
    CART_ANY,

    CART_PURPLE,
    CART_GREEN,
    CART_RED,
    CART_ORANGE,
    CART_BLACK,
    CART_WHITE,
    CART_YELLOW,
    CART_BLUE,
}
